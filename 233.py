#1-Calculate the area of a rectangle given width and height
#using return statement

def rectangle_area(height,width):
    return height*width

print(rectangle_area(5,3))

print(rectangle_area(7,2))
print(rectangle_area(10,5))






#2-Check if a number is even and return True or False
#using return statement

def even_check(number):
    return number % 2 ==0


print(even_check(4))
print(even_check(7))
print(even_check(10))

